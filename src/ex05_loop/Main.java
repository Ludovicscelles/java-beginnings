package ex05_loop;

public class Main {

  public static void main (String[] args) {
    // loop to print numbers from 1 to 10
    for (int i = 1; i <= 10; i++) {
      System.out.println(i);
    }

    // loop to print multiplication table of 7
    int n = 7;
    for (int i = 1; i <= 10; i++ ){
      System.out.println(n + " x " + i + " = " + (n * i));
    }

  }
}