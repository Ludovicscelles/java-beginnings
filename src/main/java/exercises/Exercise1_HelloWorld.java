package exercises;

/**
 * Exercise 1: Hello World
 * 
 * This is your first Java program! 
 * Learn about the basic structure of a Java program and how to print output.
 * 
 * Key Concepts:
 * - Class declaration
 * - main method (entry point)
 * - System.out.println() for printing
 */
public class Exercise1_HelloWorld {
    
    /**
     * The main method is the entry point of any Java application.
     * When you run a Java program, execution starts here.
     */
    public static void main(String[] args) {
        // Print "Hello, World!" to the console
        System.out.println("Hello, World!");
        
        // You can print multiple lines
        System.out.println("Welcome to Java programming!");
        System.out.println("This is Exercise 1.");
        
        // Challenge: Print your name below
        System.out.println("My name is: [Your Name Here]");
        
        // Print a blank line
        System.out.println();
        
        // You can also use print() without a line break
        System.out.print("This is ");
        System.out.print("on the same line. ");
        System.out.println("And this continues it!");
    }
}
